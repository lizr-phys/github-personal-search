function trend(growth: number, days: 1 | 7 | 30, quality: number): TrendWindow {
  const factor = days === 1 ? 0.16 : days === 7 ? 1 : 3.6;
  const stars = Math.max(0, Math.round(growth * factor));
  return {
    stars,
    forks: Math.round(stars * 0.12),
    activity: Math.round(10 + stars * 0.18),
    issuesAndPrs: Math.round(4 + stars * 0.08),
    heat: Number(
      Math.min(1, quality * 0.48 + Math.log10(stars + 1) / 5).toFixed(3),
    ),
  };
}

function makeRepository(seed: Seed, index: number): Repository {
  const [owner = "unknown", name = "repository"] = seed.fullName.split("/");
  const updatedDay = String(18 - (index % 12)).padStart(2, "0");
  const createdYear = 2013 + (index % 11);
  const features = [
    seed.description,
    `技术栈：${seed.technologies.slice(0, 3).join("、")}`,
    seed.deployment?.includes("self-hosted")
      ? "支持自托管"
      : `项目形态：${seed.type}`,
  ];
  return {
    id: seed.fullName.toLowerCase().replace(/[^a-z0-9]+/g, "-"),
    owner,
    name,
    fullName: seed.fullName,
    githubUrl: `https://github.com/${seed.fullName}`,
    homepageUrl: seed.deployment?.includes("web")
      ? `https://github.com/${seed.fullName}#readme`
      : undefined,
    demoUrl: seed.deployment?.includes("web")
      ? `https://github.com/${seed.fullName}#demo`
      : undefined,
    chineseTitle: seed.title,
    description: seed.description,
    summary: seed.description,
    problem: seed.description,
    targetUsers:
      seed.difficulty === "beginner"
        ? ["学习者", "独立开发者"]
        : ["开发者", "研究与工程用户"],
    coreFeatures: features,
    technologies: seed.technologies,
    language: seed.language,
    languages: [
      ...new Set(
        seed.technologies
          .filter(
            (item) =>
              !item.includes(" ") &&
              item.toLowerCase() !== seed.language.toLowerCase(),
          )
          .slice(0, 2),
      ),
    ],
    topics: seed.topics,
    domains: seed.domains,
    cluster: seed.cluster,
    projectType: seed.type,
    deployment: seed.deployment ?? ["library"],
    difficulty: seed.difficulty,
    maturity: seed.maturity,
    maintenance: index % 13 === 0 ? "slower" : "active",
    license: seed.license ?? (index % 4 === 0 ? "Apache-2.0" : "MIT"),
    stars: seed.stars,
    forks: Math.round(seed.stars * (0.08 + (index % 5) * 0.012)),
    watchers: Math.max(12, Math.round(seed.stars * 0.025)),
    trend1d: trend(seed.growth7, 1, seed.quality),
    trend7d: trend(seed.growth7, 7, seed.quality),
    trend30d: trend(seed.growth7, 30, seed.quality),
    pushedAt: `2026-07-${updatedDay}T08:00:00.000Z`,
    releasedAt:
      index % 3 === 0
        ? `2026-07-${String(Math.max(1, 15 - (index % 10))).padStart(2, "0")}T08:00:00.000Z`
        : undefined,
    createdAt: `${createdYear}-03-12T08:00:00.000Z`,
    readmeSummary: `${seed.description} 主要技术：${seed.technologies.join("、")}。安装与使用细节以仓库 README 为准。`,
    quality: seed.quality,
    novelty: Number((0.35 + ((index * 17) % 60) / 100).toFixed(2)),
    archived: false,
    mirror: false,
    fork: false,
    hasReadme: true,
    dataSource: "demo",
    dataUpdatedAt: "2026-07-18T08:00:00.000Z",
    evidence: [
      {
        type: "metadata",
        label: "仓库主题与主要语言",
        source: `demo:metadata:${seed.fullName}`,
        confidence: 0.96,
      },
      {
        type: "readme",
        label: "README 提取式摘要",
        source: `demo:readme:${seed.fullName}`,
        confidence: 0.84,
      },
      {
        type: "topic",
        label: seed.topics.slice(0, 3).join(" · "),
        source: `demo:topics:${seed.fullName}`,
        confidence: 0.92,
      },
    ],
    similar: [],
  };
}

const repositories = seeds.map(makeRepository);

for (const repository of repositories) {
  repository.similar = repositories
    .filter(
      (candidate) =>
        candidate.id !== repository.id &&
        (candidate.cluster === repository.cluster ||
          candidate.domains.some((domain) =>
            repository.domains.includes(domain),
          )),
    )
    .slice(0, 4)
    .map((candidate) => candidate.id);
}

